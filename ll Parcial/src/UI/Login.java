    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        String usuario = txtUsuario.getText();
        String pass = pfPassword.getText();
        
        boolean encontrar = true;
        for (Usuarios i : IniciarDatosUsuarios.NuevosRegistros){
            if (i.getUsuario().equals(usuario) && i.getPassword().equals(pass)){
                encontrar = true;
                break;
            }else{
                encontrar = false;
            }
        }
        
        if (encontrar){
            AgregarTareas b = new AgregarTareas(); 
            b.setVisible(true);
            this.dispose();     
        }else{
            JOptionPane.showMessageDialog(rootPane, "No se encontro el usuario en el sistema");
        }
    }//GEN-LAST:event_btnEntrarActionPerformed

