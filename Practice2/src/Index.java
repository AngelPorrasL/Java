    public void llenarDatos() throws FileNotFoundException, IOException{
        
        String linea = null;
        int numeroFilas = 0;
        //No podemos recorrer lo tenemos que llamar
        BufferedReader leer = new BufferedReader(new FileReader(archivo));
        //cuantas filas tengo
        while (leer.readLine() != null) {            
            numeroFilas = numeroFilas+1;
        }
        leer.close();
        if (numeroFilas == 0) {
          JOptionPane.showMessageDialog(rootPane, "Esta en blanco");
        }else{
            //Son los datos que estamos guardando en este momento si fueran mas tenemos que modificar el numero 4
            String[][] datos = new String[numeroFilas][4];
            //esta variable me controlada la matriz
            int posicion = 0;
            BufferedReader leerdatos = new BufferedReader(new FileReader(archivo));
            while ((linea = leerdatos.readLine()) != null) {                
                StringTokenizer st = new StringTokenizer(linea,"\t");
                
                //Guardar dentro de mi matriz la data
                datos[posicion][0] = st.nextToken();
                datos[posicion][1] = st.nextToken();
                datos[posicion][2] = st.nextToken();
                datos[posicion][3] = st.nextToken();
                posicion = posicion+1;
            }
            leerdatos.close();
            
            DefaultTableModel modelo = (DefaultTableModel) tabla.getModel();
            //ESTE FOR CONTROLA LAS FILAS
            for (int i = 0; i < datos.length; i++) {
                String []data = new String[4];
                //ESTE FOR CONTROLA LAS COLUMNAS
                for (int j = 0; j < datos[i].length; j++) {
                    data[(j)] = datos[i][j];
                }
                
                modelo.addRow(data);
                
            }
            
            
            
            

            
        }
        
        
        
    }

