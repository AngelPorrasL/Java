    public void ingresarDatos() throws FileNotFoundException, UnsupportedEncodingException, IOException{
        String nombre = txtnombre.getText();
        
        //Quien es el que escribe los datos
        BufferedWriter escribir = new BufferedWriter(new OutputStreamWriter(new FileOutputStream(archivo,true),"utf-8"));
        escribir.write(nombre+"\t"+txtapellidos.getText()+"\t"+txtcedula.getText()+"\t"+txtcorreo.getText()+"\n");
        escribir.close();
        JOptionPane.showMessageDialog(rootPane, "Datos creados correctamente");
    }
    
